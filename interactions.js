class Caroussel{
    /**
     * @type {Element[]}
     */
    images = []

    /**
     * @type {number}
     */
    currentId = 0

    /**
     * @type {EpochTimeStamp}
     */
    manualTimeout = 0

    constructor(){
        this.images = [...document.querySelectorAll(".caroussel .slide")]
        if(this.images.length == 0){
            this.disabled = true
        }
    }

    setValues(){
        if(this.disabled) return
        this.images[this.currentId].classList.toggle("presented")
        this.images.forEach((img,index)=>{
            if(index == this.currentId) return
            img.classList.remove("presented")
        })
    }
    
    startLoop(){
        if(this.disabled) return
        this.setValues()
        setInterval(()=>{
            if (Date.now() - this.manualTimeout < 10000) return
            this.next(false)
        },5000)
        return this
    }

    next(manual=true){
        this.currentId += 1
        if(this.currentId == this.images.length) this.currentId = 0
        this.setValues()
        if(manual) this.manualTimeout = Date.now()

    }

    previous(){
        this.currentId -= 1
        if(this.currentId < 0 ) this.currentId = this.images.length -1
        this.setValues()        
        this.manualTimeout = Date.now()
    }
}

class CardSpawner{
    constructor(){
        this.card = document.querySelector(".card")
        this.listType = this.card.parentElement.id
        
    }
    /**
     * @type {Element}
     */
    card

    listType="all"


    prepareCard(){
        /**
         * @type {Element}
         */
        let newCard = this.card.cloneNode(true)
        newCard.style.display = "block"
        newCard.classList.add("loading")
        newCard.style.animationDelay = Math.round(Math.random()*1000) +"ms"
        this.card.parentElement.appendChild(newCard)
        return newCard
    }
    /**
     * Set the content of the card using the bio dom
     * @param {Object} bioElement 
     * @param {Element} card
     */
    async setContent(bioElement,card){
        let img = bioElement.img || ""
        await this.preLoadImg(img)
        card.style.backgroundImage = `url('${img}')`
        card.id = bioElement.name
        card.querySelector(".legend").textContent = bioElement.name
        card.querySelector(".legend").style.bottom =`-${card.querySelector(".legend").offsetHeight}px`
        card.classList.remove("loading")
    }

    /**
     * Preload the image before showing it
     * @param {string} url 
     */
    async preLoadImg(url){
        return await new Promise((resolve, reject) => {
            const img = new Image();
            img.onload = () => resolve(img);
            img.onerror = reject;
            img.src = url
        })  
    }
}



